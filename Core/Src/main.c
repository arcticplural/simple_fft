// DAC SIN VAWE BLOCK
uint32_t sine_val[100];
#define PI 3.1415926
void calcsin ()
{
	for (int i=0; i<100; i++)
	{
		sine_val[i] = ((sin(i*2*PI/100) + 1)*(4096/2));
	}
}

